package databeans;

public class FormBean {
	private int age;
	private long beginretirementbalance;
	private long investmentgrowth;
	private long saving;
	private long Retirementspend;
	private long Accountspending;
	private long total;

	public int getAge() {
		return age;
	}

	public void setAge(int i) {
		age = i;
	}

	public long getBeginretirementbalance() {
		return beginretirementbalance;
	}

	public void setBeginretirementbalance(long l) {
		beginretirementbalance = l;
	}

	public long getInvestmentgrowth() {
		return investmentgrowth;
	}

	public void setInvestmentgrowth(long l) {
		investmentgrowth = l;
	}

	public long getSaving() {
		return saving;
	}

	public void setSaving(long l) {
		saving = l;
	}

	public long getRetirementspend() {
		return Retirementspend;
	}

	public void setRetirementspend(long l) {
		Retirementspend = l;
	}

	public long getAccountspending() {
		return Accountspending;
	}

	public void setAccountspending(long l) {
		Accountspending = l;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long l) {
		total = l;
	}

}
